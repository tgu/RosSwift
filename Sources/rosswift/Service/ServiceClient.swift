import NIO
import RosTime
import StdMsgs

enum ServiceError: Error {
    case invalidInput(String)
    case duplicateService(String)
    case noResponse
}

/// A client handle for calling a ROS service.
///
/// Obtain one from `NodeHandle.serviceClient(service:md5sum:...)`. A
/// `persistent` client keeps its connection to the service server open
/// across calls; a non-persistent one connects per call.
public final class ServiceClient {

    var serverLink: ServiceServerLink?
    let name: String
    let persistent: Bool
    let headerValue: StringStringMap?
    let serviceMd5sum: String
    var isShutdown: Bool = false
    let ros: Ros
    
    deinit {
        shutdown()
    }
    
    func shutdown() {
        if !isShutdown {
            if !persistent {
                isShutdown = true
            }
            serverLink = nil
        }
    }
    
    func isValid() -> Bool {
        if !persistent {
            return true
        }
        if isShutdown {
            return false
        }
        if let link = serverLink {
            return link.isValid()
        }
        return false
    }
    
    
    init(ros: Ros, name: String, md5sum: String, persistent: Bool = false, headerValues: StringStringMap? = nil) async {
        self.ros = ros
        self.name = name
        self.persistent = persistent
        self.headerValue = headerValues
        self.serviceMd5sum = md5sum
        
        if persistent {
            serverLink = await ros.serviceManager.createServiceServerLink(
                service: name,
                persistent: persistent,
                requestMd5sum: serviceMd5sum,
                responseMd5sum: serviceMd5sum,
                headerValues: headerValue)
        }
    }
    
    /// Calls the service with `req` and returns the typed response.
    ///
    /// Request and response types must come from the same `.srv` definition
    /// (their md5sums must match, or this traps).
    /// - Parameter req: The request message.
    /// - Returns: The decoded response message.
    /// - Throws: A `ServiceError` if the call fails or the response can't be
    ///   deserialized.
    public func call<MReq: ServiceMessage, MRes: ServiceMessage>(req: MReq) async throws -> MRes {
        if MReq.srvMd5sum != MRes.srvMd5sum {
            fatalError("The request and response parameters to the service " +
                       "call must be autogenerated from the same " +
                       "server definition file (.srv). your service call " +
                       "for \(name) appeared to use request/response types " +
                       "from different .srv files. (\(MReq.srvMd5sum) vs. \(MRes.srvMd5sum)")
        }
        let serializedRequest = SerializedMessage(msg: req)

        let ser = try await call(req: serializedRequest, serviceMd5sum: MReq.srvMd5sum).get()
        do {
            let res: MRes = try deserializeMessage(m: ser)
            return res
        } catch {
            ROS_ERROR("Exception thrown while while deserializing service call: \(error)")
            throw ServiceError.noResponse
        }
    }
    
    func call(req: consuming SerializedMessage, serviceMd5sum: String) async -> EventLoopFuture<SerializedMessage> {
        
        let eventLoop = threadGroup.next()
        let promise: EventLoopPromise<SerializedMessage> = eventLoop.makePromise()
        
        guard self.serviceMd5sum == serviceMd5sum else {
            let err = ServiceError.invalidInput("Call to service [\(name)]" +
                                                " with md5sum [\(serviceMd5sum)] does not match md5sum when the handle was" +
                                                " created ([\(self.serviceMd5sum)])")
            promise.fail(err)
            return promise.futureResult
        }
        var link: ServiceServerLink?
        
        if persistent {
            if serverLink == nil {
                serverLink = await ros.serviceManager.createServiceServerLink(
                    service: name,
                    persistent: persistent,
                    requestMd5sum: serviceMd5sum,
                    responseMd5sum: serviceMd5sum,
                    headerValues: headerValue)
                
                if serverLink == nil {
                    promise.fail(ServiceError.invalidInput("Could not create ServiceServerLink"))
                    return promise.futureResult
                }
            }
            link = serverLink
        } else {
            link = await ros.serviceManager.createServiceServerLink(
                service: name,
                persistent: persistent,
                requestMd5sum: serviceMd5sum,
                responseMd5sum: serviceMd5sum,
                headerValues: headerValue)
            if link == nil {
                promise.fail(ServiceError.invalidInput("Could not create ServiceServerLink"))
                return promise.futureResult
            }
        }
        promise.succeed(SerializedMessage())
        
        return link!.call(req: req)
    }
    
    
    /// Waits until the service is advertised and available, or the timeout elapses.
    ///
    /// - Parameters:
    ///   - ros: The node used to query the master.
    ///   - timeout: How long to wait. A negative duration (the default) waits
    ///     indefinitely (until the node shuts down).
    /// - Returns: `true` if the service became available, `false` on timeout.
    public func waitForExistence(ros: Ros, timeout: RosDuration = RosDuration(seconds: -1)) async -> Bool {
        return await Service.waitForService(ros: ros, serviceName: name, timeout: timeout)
    }

    /// Returns whether the service is currently advertised and reachable.
    public func exists() async -> Bool {
        return await Service.exists(ros: ros, serviceName: name, printFailureReason: false)
    }

    /// The resolved name of the service this client targets.
    public func getService() -> String {
        return name
    }
    
}
